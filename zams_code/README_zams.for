This is not the original zams code distributed by Hansen and Kawaler. Anil has edited the code so that every value may be input on one line.

The revised version of ZAMS expects all the inputs on one line, as follows:
Solar masses | X (Hyd abundance) | Y (He abundance) | Central pressure | Central Temperature | Radius | Solar Lumiosities | output file | want pulsation? (y/n)

Note: Write scientific notation as "1.422e7".
Table C.1 in Appendix C on page 430 of Hansen and Kawaler shows some initial guesses that converge for some masses of stars.
